package d16DoWhileLoops;

public class DoWhileLoop01 {
    public static void main(String[] args) {
        //Example 1; print the numbers from 5 to 3 on the console.....

        int i=5;
        do {
            System.out.print(i+" ");
            i--;
        } while (i>2);
        System.out.println("===========================");

        int k=5;

        do {
            System.out.print(k+" ");
            k++;

        }while (k<10);
        System.out.println("=================");







    }
}
